//json body
func (ctx *Context) BindJson(obj any) error {
	if ctx.request != nil {
		body, err := ioutil.ReadAll(ctx.request.Body)
		if err != nil {
			return err
		}
		// ctx.request.Body只能被读一次 需要再设一次
		ctx.request.Body = ioutil.NopCloser(bytes.NewBuffer(body))
		// 将body序列号
		err = json.Unmarshal(body, obj)
		if err != nil {
			return err
		}
	} else {
		return errors.New("ctx.request empty")
	}
	return nil
}

func (ctx *Context) BindXml(obj any) error {
	if ctx.request != nil {
		body, err := ioutil.ReadAll(ctx.request.Body)
		if err != nil {
			return err
		}
		ctx.request.Body = ioutil.NopCloser(bytes.NewBuffer(body))
		err = xml.Unmarshal(body, obj)
		if err != nil {
			return err
		}
	} else {
		return errors.New("ctx.request empty")
	}
	return nil
}
func (ctx *Context) GetRawData() ([]byte, error) {
	if ctx.request != nil {
		body, err := ioutil.ReadAll(ctx.request.Body)
		if err != nil {
			return nil, err
		}
		//重设body
		ctx.request.Body = ioutil.NopCloser(bytes.NewBuffer(body))
		return body, nil
	}
	return nil, errors.New("ctx.request empty")
}

// 基础信息
//Uri() string
func (ctx *Context) Uri() string {
	return ctx.request.RequestURI
}

//Method() string

func (ctx *Context) Method() string {

	return ctx.request.Method
}

func (ctx *Context) Host() string {
	// 为什么不是直接  ctx.request.Host
	return ctx.request.URL.Host
}

//ClientIp() string
func (ctx *Context) ClientIp() string {
	r := ctx.request
	//  为什么不是直接 ctx.request.RemoteAddr
	ipAddr := r.Header.Get("X-Real-Ip")
	if ipAddr == "" {
		ipAddr = r.Header.Get("X-Forwarded-For")
	}
	if ipAddr == "" {
		ipAddr = r.RemoteAddr
	}
	return ipAddr
}

//Header
func (ctx *Context) Headers() map[string][]string {
	return ctx.request.Header
}

func (ctx *Context) Header(key string) (string, bool) {
	vals := ctx.request.Header.Values(key)
	if len(vals) <= 0 || vals == nil {
		return "", false
	}
	return vals[0], true
}

func (ctx *Context) Cookies() map[string]string {
	cookies := ctx.request.Cookies()
	ret := map[string]string{}
	for _, cookie := range cookies {
		ret[cookie.Name] = cookie.Value
	}
	return ret
}

func (ctx *Context) Cookie(key string) (string, bool) {
	if val, ok := ctx.Cookies()[key]; ok {
		return val, true
	}
	return "", false
}
